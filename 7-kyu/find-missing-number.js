"use strict";

/**
 * O(n) complexity
 * @param {array} nums - unsorted numbers from 0 - 100 inclusive, but one number is missing
 * @returns {number} - missing number 
 */
function missingNo(nums) {
    let missingNum;

    const sortedNums = [...nums].sort((a, b) => a - b);

    for (let i = 0; i <= 100; i++) {
        if (i !== sortedNums[i]) {
            missingNum = i;
    
            break;
        }
    }
    
    return missingNum;
}

